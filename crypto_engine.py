RCON = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]

def gf_mul2(value):
    result = value << 1
    if result & 0x100:
        result ^= 0x11B
    return result & 0xFF

def gf_mul3(value):
    return gf_mul2(value) ^ value

def text_to_bytes_16(text):
    return list(text.encode("utf-8").ljust(16, b"\0")[:16])

def bytes_to_state(byte_list):
    state = [[0] * 4 for _ in range(4)]
    for i, b in enumerate(byte_list):
        state[i % 4][i // 4] = b
    return state

def state_to_hex(state):
    values = []
    for col in range(4):
        for row in range(4):
            values.append(f"{state[row][col]:02X}")
    return " ".join(values)

def sub_bytes(state):
    return [[AES_SBOX[state[r][c]] for c in range(4)] for r in range(4)]

def shift_rows(state):
    new_state = [[0] * 4 for _ in range(4)]
    new_state[0] = state[0][:]
    new_state[1] = state[1][1:] + state[1][:1]
    new_state[2] = state[2][2:] + state[2][:2]
    new_state[3] = state[3][3:] + state[3][:3]
    return new_state

def mix_columns(state):
    new_state = [[0] * 4 for _ in range(4)]

    for c in range(4):
        s0, s1, s2, s3 = state[0][c], state[1][c], state[2][c], state[3][c]

        new_state[0][c] = gf_mul2(s0) ^ gf_mul3(s1) ^ s2 ^ s3
        new_state[1][c] = s0 ^ gf_mul2(s1) ^ gf_mul3(s2) ^ s3
        new_state[2][c] = s0 ^ s1 ^ gf_mul2(s2) ^ gf_mul3(s3)
        new_state[3][c] = gf_mul3(s0) ^ s1 ^ s2 ^ gf_mul2(s3)

    return new_state

def add_round_key(state, round_key):
    new_state = [[0] * 4 for _ in range(4)]

    for c in range(4):
        for r in range(4):
            new_state[r][c] = state[r][c] ^ round_key[c * 4 + r]

    return new_state

def rot_word(word):
    return word[1:] + word[:1]

def sub_word(word):
    return [AES_SBOX[b] for b in word]

def key_expansion(key_text):
    key = text_to_bytes_16(key_text)
    expanded = key[:]

    bytes_generated = 16
    rcon_iteration = 1

    while bytes_generated < 176:
        temp = expanded[bytes_generated - 4:bytes_generated]

        if bytes_generated % 16 == 0:
            temp = sub_word(rot_word(temp))
            temp[0] ^= RCON[rcon_iteration]
            rcon_iteration += 1

        for i in range(4):
            expanded.append(expanded[bytes_generated - 16] ^ temp[i])
            bytes_generated += 1

    return [expanded[i:i + 16] for i in range(0, 176, 16)]

def run_aes_simulation(plaintext, key):
    steps = []

    state = bytes_to_state(text_to_bytes_16(plaintext))
    round_keys = key_expansion(key)

    steps.append(f"Giriş Plaintext Matrisi: {state_to_hex(state)}")

    state = add_round_key(state, round_keys[0])
    steps.append(f"Başlangıç AddRoundKey: {state_to_hex(state)}")

    for round_no in range(1, 10):
        steps.append(f"--- AES Round {round_no} ---")

        state = sub_bytes(state)
        steps.append(f"Round {round_no} - SubBytes: {state_to_hex(state)}")

        state = shift_rows(state)
        steps.append(f"Round {round_no} - ShiftRows: {state_to_hex(state)}")

        state = mix_columns(state)
        steps.append(f"Round {round_no} - MixColumns: {state_to_hex(state)}")

        state = add_round_key(state, round_keys[round_no])
        steps.append(f"Round {round_no} - AddRoundKey: {state_to_hex(state)}")

    steps.append("--- AES Final Round 10 ---")

    state = sub_bytes(state)
    steps.append(f"Round 10 - SubBytes: {state_to_hex(state)}")

    state = shift_rows(state)
    steps.append(f"Round 10 - ShiftRows: {state_to_hex(state)}")

    state = add_round_key(state, round_keys[10])
    steps.append(f"Round 10 - AddRoundKey: {state_to_hex(state)}")

    cipher_text = state_to_hex(state)
    steps.append(f"Nihai AES-128 Ciphertext: {cipher_text}")

    return cipher_text, steps